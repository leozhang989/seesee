    <h3 id="推荐扫二维码配置">推荐，扫二维码配置</h3>

    <p>1、首先网页上打开节点的【二维码】图片</p>

    <p>2、然后打开软件左上角/右下角的“+”，选择“扫描屏幕上的二维码”</p>

    <p>3、程序自动识别二维码并导入服务器节点信息</p>

    <p>4、最后点击 【小飞机】 的标志 即可连接上网了</p>

